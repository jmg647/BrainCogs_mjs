function rigID = getRigID(rigNum)

switch rigNum
    case 'rig1', rigID = 'TrainVR1';
    case 'rig2', rigID = 'TrainVR2';
    case 'rig3', rigID = 'TrainVR3';
    case 'rig4', rigID = 'TrainVR4';
    case 'rig5', rigID = 'VRTrain5';
    case 'rig6', rigID = 'VRTrain6';
    case 'rig7', rigID = 'VRTrain7';
end